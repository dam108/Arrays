package arrays;

public class AgendaTelefonica {
    private String nombre;
    private int telefono;
    /*CONSTRUCTOR*/
    AgendaTelefonica (String n, int t){
         nombre = n;
         telefono = t;
    }

    /* FIN CONSTRUCTORES */

    /*METODOS*/

    /* GETTERS Y SETTERS */

    public String getNombre() {
        return nombre;
    }

    public int getTelefono() {
        return telefono;
    }

} // fin class
